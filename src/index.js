const MORSE_TABLE = {
    '.-':     'a',
    '-...':   'b',
    '-.-.':   'c',
    '-..':    'd',
    '.':      'e',
    '..-.':   'f',
    '--.':    'g',
    '....':   'h',
    '..':     'i',
    '.---':   'j',
    '-.-':    'k',
    '.-..':   'l',
    '--':     'm',
    '-.':     'n',
    '---':    'o',
    '.--.':   'p',
    '--.-':   'q',
    '.-.':    'r',
    '...':    's',
    '-':      't',
    '..-':    'u',
    '...-':   'v',
    '.--':    'w',
    '-..-':   'x',
    '-.--':   'y',
    '--..':   'z',
    '.----':  '1',
    '..---':  '2',
    '...--':  '3',
    '....-':  '4',
    '.....':  '5',
    '-....':  '6',
    '--...':  '7',
    '---..':  '8',
    '----.':  '9',
    '-----':  '0',
};

function decode(expr) {
    var phrase = "";
	var space = "**********";
	
	var encoded_letters = expr.match(/.{1,10}/g);
	
	encoded_letters.forEach(function(encoded_letter) { 
		if (encoded_letter === space) {
			phrase += " ";
		} else {
			morse_letter = encoded_letter.replace(/00/g, '').replace(/10/g, '.').replace(/11/g, '-'); 
			phrase += MORSE_TABLE[morse_letter];
		}
	});
	
	return phrase;
}

module.exports = {
    decode
}